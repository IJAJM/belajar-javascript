var x;
//pre-increment
console.log('pre-increment');
x = 9;
console.log(`x \t : ${x}`);
console.log(`++x \t : ${++x}`);
console.log(`x \t : ${x}`);

//post-increment
console.log('\nPost-increment');
x = 9;
console.log(`x \t : ${x}`);
console.log(`x++ \t : ${x++}`);
console.log(`x \t : ${x}`);