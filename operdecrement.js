var x;
//pre-decrement
console.log('pre-decrement');
x = 9;
console.log(`x \t: ${x}`);
console.log(`--x \t: ${--x}`);
console.log(`x \t: ${x}`);

//post-decrement
console.log('\nPost-decrement');
x = 9;
console.log(`x \t: ${x}`);
console.log(`x-- \t: ${x--}`);
console.log(`x \t: ${x}`);